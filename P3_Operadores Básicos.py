# --- Datos personales pARA PRACTICAER ---
edad = 21                 # Literal entero que representa mi edad
peso = 66.0               # Literal flotante que representa mi peso en kilogramos
altura = 1.71             # Literal flotante que representa mi altura en metros

# --- Operadores básicos ---

# Suma
suma = edad + 5           # Sumo 5 a mi edad
print("Edad + 5 =", suma) # Imprimo el resultado de la suma

# Resta
resta = peso - 10          # Resto 10 a mi peso
print("Peso - 10 =", resta) # Imprimo el resultado de la resta

# Multiplicación
multiplicacion = altura * 2   # Multiplico mi altura por 2
print("Altura * 2 =", multiplicacion) # Imprimo el resultado

# División
division = peso / altura      # Divido mi peso entre mi altura
print("Peso / Altura =", division) # Imprimo el resultado

# División entera
division_entera = peso // altura   # Divisi_
